using namespace std;

int main(){
	int a,b,c;
	cin >> a >> b >> c;
	
	if(a>b && a>c){
		if(b<c){
			cout << a-b;
		}}
		else if(b>a && b>c){
			if(a<c){
				cout << b-a;
			}}
			
		else if(c>a && c>b){
			if(a<b){
				cout << c-a;
			}
		}
			
		
		
	
	
	
	
}
